namespace Win32
{
	/// <summary>
	/// Specifies whether collation should be used when printing multiple copies.
	/// </summary>
	public enum DMCOLLATE : short
	{
		/// <summary>
		/// Do not collate when printing multiple copies.
		/// </summary>
		FALSE=0,

		/// <summary>
		/// Collate when printing multiple copies.
		/// </summary>
		TRUE=1,
	}
}
